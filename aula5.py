# operadodes aritimeticos 
adicao = 10 + 10 
print('adicço', adicao)

subtracao = 10 - 5
print('subtraçao', subtracao) 

divisao = 10 / 2.2 # float
print('Divisão', divisao)

divisao_inteira = 10 // 2.2
print('Divisão inteira', divisao_inteira) # O // corta todo o resto da divisão ex ao enves de aparecer 4.5 vai aparecer 4

expponeciacao = 2 ** 10 
print('Ecponeciação', expponeciacao)

modulo = 25  % 5 # resto da divisão ex 
print('Módulo ', modulo)
