# operações codicionais 
#if / elif / else
# se / se não / se não
#o else sempre sera a ultima opção
entrada = input('Você quer "entrar" ou "sair" ?')
if entrada == 'entrar':
    print('Você entrou no sistema')
elif entrada == 'sair':
    print('Você saiu do sistema ')
else:
    print('Você não digitou nen entrar ou sair ')
